package week1.day1;

public class DataTypes {
	public static void main(String[] args) {
		//DataTypes
		byte age=32;
		System.out.println("age");
		System.out.println(age);
		
		int noOfStudents=123456789;
		System.out.println(noOfStudents);
		
		// to create a long the value should consist suffix 'L' at last
		long phNo=123456789L;
		System.out.println(phNo);
		
		short regNo=12345;
		System.out.println(regNo);
		
		// to create a float the value should consist suffix 'f' at last
		float speed=3.65712973567f;
		System.out.println(speed);
		
		double speed1=3.65712973567;
		System.out.println(speed1);
		
		char logo='s';
		System.out.println(logo);
		 
		boolean isvisible=true;
		System.out.println(isvisible);
		
		
		String name="Harishma";
		System.out.println(name);
		
	}
}
